
import java.util.*;

public class nyobafor{

  public static void main(String[] args) {
    int max = 10;

    for (int i = 0; i < max; i+=2) {
      System.out.print(i);
      System.out.print(" ");
    }
    System.out.println("");
    for (int i = 2; i < 12 ; i+=2) {
      System.out.print(i);
      System.out.print(" ");
    }
    System.out.println("");
    for (int i = 4; i < max+4 ; i+=2) {
      System.out.print(i);
      System.out.print(" ");
    }
    System.out.println("");
    for (int i = 6; i < 12 ; i+=2) {
      System.out.print(i);
      System.out.print(" ");
    }
    System.out.println("");
}
}
